package com.study.ch04;

public class Operation09 {
    public static void main(String[] args) {
        /*
        << 증감연산자>>
        */

        int i = 0;
        i += 1; // i에서 1을 더해서 대입.
        i++;
        System.out.println(++i); // 선증강
        System.out.println(i++); // 후증강
        System.out.println(i--);
        System.out.println(i);




    }
}
